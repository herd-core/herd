//go:build linux

// seccomp_linux.go — seccomp BPF syscall filter for herd workers.
//
// # Overview
//
// Seccomp (Secure Computing mode) restricts which syscalls a process can
// invoke. Herd uses an allowlist: the worker process is permitted only the
// syscalls a typical HTTP server needs. Every other syscall returns EPERM
// (SeccompPolicyErrno) or kills the process (SeccompPolicyKill).
//
// # Threading note
//
// Go multiplexes goroutines onto OS threads (M:N model). The BPF filter must
// be installed with SECCOMP_FILTER_FLAG_TSYNC so it is applied to all OS
// threads the Go runtime creates — not just the one that called the syscall.
// go-seccomp-bpf handles this automatically.
//
// # Worker integration
//
// [ProcessFactory] injects HERD_SECCOMP_PROFILE into the worker's environment.
// The worker binary calls [EnterSandbox] at the top of main() to install the
// filter. This design keeps the filter in the child process and avoids
// contaminating the herd parent process.
package herd

import (
	"fmt"
	"os"

	seccomp "github.com/elastic/go-seccomp-bpf"
	"golang.org/x/net/bpf"
)

// SeccompPolicy controls how unauthorized syscalls are handled.
type SeccompPolicy int

const (
	// SeccompPolicyOff disables seccomp filtering entirely.
	// Workers that do not call EnterSandbox should use this policy.
	SeccompPolicyOff SeccompPolicy = iota

	// SeccompPolicyLog logs blocked syscalls via the kernel audit subsystem
	// but does not block them. Requires Linux 4.14+. Useful during development
	// to discover which syscalls a new worker needs.
	SeccompPolicyLog

	// SeccompPolicyErrno returns EPERM for any unauthorized syscall.
	// This is the default for production workers.
	SeccompPolicyErrno

	// SeccompPolicyKill terminates the process immediately on any unauthorized
	// syscall. Strictest policy; no error is returned to the caller.
	SeccompPolicyKill
)

// envValue returns the string written into HERD_SECCOMP_PROFILE.
func (p SeccompPolicy) envValue() string {
	switch p {
	case SeccompPolicyLog:
		return "log"
	case SeccompPolicyErrno:
		return "errno"
	case SeccompPolicyKill:
		return "kill"
	default:
		return "off"
	}
}

// parsePolicyEnv converts the HERD_SECCOMP_PROFILE value back to a SeccompPolicy.
func parsePolicyEnv(s string) SeccompPolicy {
	switch s {
	case "log":
		return SeccompPolicyLog
	case "errno":
		return SeccompPolicyErrno
	case "kill":
		return SeccompPolicyKill
	default:
		return SeccompPolicyOff
	}
}

// defaultSeccompAction maps a SeccompPolicy to a go-seccomp-bpf Action.
func defaultSeccompAction(p SeccompPolicy) seccomp.Action {
	switch p {
	case SeccompPolicyLog:
		return seccomp.ActionLog
	case SeccompPolicyKill:
		return seccomp.ActionKillProcess
	default: // SeccompPolicyErrno
		return seccomp.ActionErrno
	}
}

