// EnterSandbox installs the seccomp syscall filter for the current process.
// Worker binaries should call this at the very top of main(), before starting
// any goroutines or HTTP servers.
//
// The filter policy is read from the HERD_SECCOMP_PROFILE environment variable,
// which [ProcessFactory] injects automatically. If the variable is absent or
// set to "off", EnterSandbox is a no-op.
//
// EnterSandbox installs the filter with SECCOMP_FILTER_FLAG_TSYNC, which
// synchronises the filter across all OS threads created by the Go runtime.
//
// Example worker main():
//
//	func main() {
//	    if err := herd.EnterSandbox(); err != nil {
//	        log.Fatalf("seccomp: %v", err)
//	    }
//	    // ... start HTTP server ...
//	}
func EnterSandbox() error {
	profileEnv := os.Getenv("HERD_SECCOMP_PROFILE")
	if profileEnv == "" || profileEnv == "off" {
		return nil
	}

	policy := parsePolicyEnv(profileEnv)
	if policy == SeccompPolicyOff {
		return nil
	}

	filter := seccomp.Filter{
		NoNewPrivs: true, // belt-and-suspenders: also set by SysProcAttr in parent
		Flag:       seccomp.FilterFlagTSync,
		Policy: seccomp.Policy{
			DefaultAction: defaultSeccompAction(policy),
			Syscalls: []seccomp.SyscallGroup{
				{
					Action: seccomp.ActionAllow,
					Names:  httpWorkerSyscalls,
				},
			},
		},
	}

	if err := seccomp.LoadFilter(filter); err != nil {
		return fmt.Errorf("herd: EnterSandbox: load seccomp filter: %w", err)
	}
	return nil
}

// buildHTTPWorkerFilter builds but does not install the BPF filter for the
// http-worker policy. Used in tests to validate the BPF program structure.
func buildHTTPWorkerFilter(policy SeccompPolicy) ([]bpf.Instruction, error) {
	f := seccomp.Filter{
		NoNewPrivs: false,
		Flag:       seccomp.FilterFlagTSync,
		Policy: seccomp.Policy{
			DefaultAction: defaultSeccompAction(policy),
			Syscalls: []seccomp.SyscallGroup{
				{
					Action: seccomp.ActionAllow,
					Names:  httpWorkerSyscalls,
				},
			},
		},
	}
	return f.Policy.Assemble()
}
